<template>
  <div>
    <div class="todo-header">
      <input
        type="text"
        v-model="title"
        @keydown.enter="add"
        placeholder="请输入你的任务名称，按回车键确认"
      />
    </div>
  </div>
</template>

<script>
import { nanoid } from "nanoid";
//引入组件
export default {
  name: "MyHeader",
  props: ["todos", "addtodo"],
  data() {
    return {
      title: "",
    };
  },
  methods: {
    add() {
      if (!this.title.trim) return alert("请输入一个数据");
      const todoObject = { id: nanoid(), title: this.title, isDone: false };
      this.addtodo(todoObject);
      this.title = "";
    },
  },
};
</script>
